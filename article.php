          <h2 class="post-title">What the NBA can teach us about fundraising</h2>
          <p class="post-meta"><span class="post-date">Feb 19, 2015</span> &bull; <a href="">Ecommerce and Online Giving</a>, <a href="">Nonprofit Marketing</a></p>
          <figure class="post-image">
            <img src="/assets/images/blog-pic-01.jpg" alt="">
          </figure>
          <div class="body post-body">
            <p>I am a huge sports fan and during basketball season, the Atlanta Hawks are my team. And although they may not always be a contender in the playoffs, this year, they are certainly making a name for themselves.</p>
            <p>Your nonprofit may be in a similar boat. Maybe you are doing great things but you don?t have the recognition such as the Chicago Bulls or a star player like Lebron James. I?m here to tell you that it doesn?t matter?you can still do great things and raise big dollars by taking a few tips from those who have been through similar struggles.</p>
            <h3>Small but mighty</h3>
            <p>Like the Atlanta Hawks, you may not be on everyone?s short list when it comes to top nonprofit organizations?but you don?t need to be big to be mighty. Below are 5 fundraising ideas for how to raise money online with a little guidance from the NBA.</p>
            <h4>1. Teamwork</h4>
            <p>It's not about your name; it?s about how you work together. Sure, we all want name recognition (and you may very well get there) but it?s the teamwork that will push you to the top. The Atlanta Hawks went 17-0 in the month of January, the best month in NBA history. How did they do it? Unbelievable teamwork. The first step to successful fundraising is to make sure your team is set up for success. Believing in the mission and what you?ve set out to do when it comes to donations is important; knowing when you can leverage others in your group is important as well. Those organizations that operate as a true team will yield better donation results than those that do not.<p>
            <h4>2. A winning attitude</h4>
            <p>Positivity goes a long way when it comes to making change. Think about it: when you see a professional athlete step into the arena prior to game time, they?re usually dressed nicely. They may even have headphones on with music that gets them pumped up for the game. And when they come onto the court before the game starts, they are running?clapping?smiling?giving their teammates high-fives. They are positive. The same holds true in fundraising. If you go into your annual goal thinking it?s a stretch, thinking that you can?t do it or getting frustrated with the tools you have, you?re setting yourself up for failure. Get excited about what you?re trying to achieve and come at it with a positive attitude. Look for the tools that will get you there and use them as your ?warm-up? throughout the year.</p>
            <h4>3. Access to Facilities</h4>
            <p>Professional sports players don?t get good at what they do without practice?and they have access to some of the world?s greatest practice facilities. Think in this same way when it comes to your donors. Give them easy access to your online donation forms wherever they may be visiting you from?be it social media, their mobile device or a desktop. Take it one step further by hooking up social media links on your donation pages and nonprofit website so that your donors can share your message with their networks. Other ways to give your donors easy access to donation options include monthly giving options and recurring donations.</p>
            <h4>4. Balance</h4>
            <p>In the case of the Atlanta Hawks, they don?t have one ?star? player on their team. The players are equally matched and each carry their own weight on the team. This is true for any successful fundraising team. Divvy up the priorities and communicate with one another to make it happen. Playing well together (working well together) is of utmost importance. If you?re able to divide tasks, assign someone who is in charge of direct mailings and traditional fundraising tactics, another who is in charge of social and peer-to-peer fundraising and yet another who could manage the daily online giving tools. But make sure that these roles don?t operate in silos?you are truly a team and should communicate for a nice balance in working together to be successful.</p>
            <p>Also, think about whose role it is to coach your team?they have the resources to help your team stay on track, monitor results and push you to reach your goals. They are the person who is charged with making your team better as you move through the fundraising year.</p>
            <h4>5. Celebration</h4>
            <p>Celebrate your small victories (and that includes the close games!) What?s the saying? It?s not about winning or losing, but how you played the game. In the end, if you can honestly assess your fundraising initiatives and say that you gave it your all and did all you could to drive donations for your organization, you?ve reached success. Leverage that success for the following year and build goals for where you want to be.</p>
            <p>You chose to work in nonprofits for a reason and you believe in the mission of your organization. Fundraising can be fun and provides you with creative opportunities to spread your organization?s great message. If you?re looking for new ways to implement online fundraising into your plan for this year, give us a call. We would be happy to help you get on track for a winning season!</p> 
